package beans;

public class RecommendProductBean {
 public Integer recommendProductId;
 public String category;
 public String description;
 public String address;
 public String imageString;
 public String price;
 public String imageURL;
 public Integer registrationId;
public Integer getRecommendProductId() {
	return recommendProductId;
}
public void setRecommendProductId(Integer recommendProductId) {
	this.recommendProductId = recommendProductId;
}
public String getCategory() {
	return category;
}
public void setCategory(String category) {
	this.category = category;
}
public String getDescription() {
	return description;
}
public void setDescription(String description) {
	this.description = description;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public String getImageString() {
	return imageString;
}
public void setImageString(String imageString) {
	this.imageString = imageString;
}
public String getPrice() {
	return price;
}
public void setPrice(String price) {
	this.price = price;
}
public String getImageURL() {
	return imageURL;
}
public void setImageURL(String imageURL) {
	this.imageURL = imageURL;
}
public Integer getRegistrationId() {
	return registrationId;
}
public void setRegistrationId(Integer registrationId) {
	this.registrationId = registrationId;
}
 
}
